<script>
import http from "../http.js";

export default {
  name: "top_bar",
  data() {
    return {
      activeIndex: "/",
      searchText: "",
      user_nick: ""
    };
  },
  methods: {
    getUsers() {
      http
        .fetchPost("/user/getUsers")
        .then(data => {
          console.log(data);
        })
        .catch(err => console.log(err));
    },
    handleSelect(index) {
      this.activeIndex = index;
      //当前地址栏url更改
      this.$router.push(index);
    },
    login() {
      this.$router.push("/login");
    },
    toRegister() {
      this.$router.push("/register");
    },
    //注销登陆
    logout(){
      localStorage.setItem("user",null);
      this.$router.go(0)
    },
    search() {
      console.log(this.searchText);
    },
    showMyMsg(){
      this.$router.push("/my");
    },
    //我的信息下拉栏选择
    handleCommand(index){
      switch(index){
        case "/myPage":
          break;
        case "/showLike":
          break;
        case "/myMsg":
          this.showMyMsg();
          break;
        case "/logout":
          this.logout();
          break;
      }
    },
    init() {
      var user = JSON.parse(localStorage.getItem("user"));
      this.user_nick = user["nick"];
      console.log("nick=" + this.user_nick);
    }
  },
  mounted() {
    this.init();
  },
  computed: {
    newMsg () {
      if(this.$store.state.my_unchecked_msg==null) return 0;
      return this.$store.state.my_unchecked_msg.payload.object.length;
    }
  }
};
</script>

